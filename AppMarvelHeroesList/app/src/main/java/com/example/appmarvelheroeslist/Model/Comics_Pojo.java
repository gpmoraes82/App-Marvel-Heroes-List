package com.example.appmarvelheroeslist.Model;

public class Comics_Pojo {

    private String comicTitle;
    private String comicImageUrl;

    public Comics_Pojo(String comicTitle, String comicImageUrl) {
        this.comicTitle = comicTitle;
        this.comicImageUrl = comicImageUrl;
    }

    public String getComicTitle() {
        return comicTitle;
    }

    public String getComicImageUrl() {
        return comicImageUrl;
    }



}
